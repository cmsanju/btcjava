package com.test;

public class Exp1 {
	
	public static void main(String[] args) {
		
		int i = 0;
		
		for( i = 0; i <= 10; i++)
		{
		  System.out.println(i);
		}
		
		System.out.println(i);// 0 
		
		int y = 20;
		
		while(y >= 10)
		{
			System.out.println(y);
			
			y--;//9
		}
		
		System.out.println(y);
	}

}
